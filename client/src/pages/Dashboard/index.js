const Dashboard = () => {
  const [currentPage, setCurrentPage] = useState(1);
  const [searchQuery, setSearchQuery] = useState("");
  const [filteredData, setFilteredData] = useState([]);
  const itemsPerPage = 6;
  const pagesToShow = 5; // Number of page buttons to show

  // Calculate the start and end indices for the current page
  const startIndex = (currentPage - 1) * itemsPerPage;
  const endIndex = startIndex + itemsPerPage;

  // Get the data to display on the current page
  const currentPageData = filteredData.slice(startIndex, endIndex);

  const totalPages = Math.ceil(filteredData.length / itemsPerPage);

  const handlePageChange = (newPage) => {
    setCurrentPage(newPage);
  };

  useEffect(() => {
    // Filter data based on the search query
    const filtered = data.filter((person) =>
      person.name.toLowerCase().includes(searchQuery.toLowerCase())
    );
    setFilteredData(filtered);
    setCurrentPage(1); // Reset to the first page when the search query changes
  }, [searchQuery]);

  const canGoPrevious = currentPage > 1;
  const canGoNext = currentPage < totalPages;

  const renderPagination = () => {
    const pageButtons = [];

    for (let i = 1; i <= totalPages; i++) {
      if (
        i === 1 ||
        i === totalPages ||
        (i >= currentPage - 2 && i <= currentPage + 2)
      ) {
        // Show first, last, and nearby pages
        pageButtons.push(
          <a
            key={i}
            onClick={() => handlePageChange(i)}
            className={
              "relative inline-flex items-center px-4 py-2 text-sm font-semibold " +
              (currentPage === i
                ? "bg-red-600 text-white focus:z-20 focus-visible:outline focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-600"
                : "text-gray-900 ring-1 ring-inset ring-gray-400 hover:bg-red-400 focus:z-20 focus:outline-offset-0")
            }
          >
            {i}
          </a>
        );
      } else if (pageButtons[pageButtons.length - 1] !== "...") {
        // Add ellipsis if not already added
        pageButtons.push(<span key={i}>...</span>);
      }
    }

    return pageButtons;
  };

  return (
    <div className="dashBoard">
      <div className="searchContainer">
        <form
          method="GET"
          onSubmit={(e) => {
            e.preventDefault(); // Prevent the form from submitting
            // Set the search query to the input value
            setSearchQuery(e.target.elements.q.value);
          }}
        >
          <div className="relative text-gray-400 focus-within:text-gray-400">
            <span className="absolute inset-y-0 left-0 flex items-center pl-2">
              <button
                type="submit"
                className="p-1 focus:outline-none focus:shadow-outline"
              >
                <svg
                  fill="none"
                  stroke="currentColor"
                  stroke-linecap="round"
                  stroke-linejoin="round"
                  stroke-width="2"
                  viewBox="0 0 24 24"
                  className="w-6 h-6"
                >
                  <path d="M21 21l-6-6m2-5a7 7 0 11-14 0 7 7 0 0114 0z"></path>
                </svg>
              </button>
            </span>
            <input
              type="search"
              name="q"
              className="py-2 text-sm text-white bg-gray-900 rounded-md pl-10 focus:outline-none focus:bg-white focus:text-gray-900"
              placeholder="Search..."
              autoComplete="off"
            />
          </div>
        </form>

        <button className="bg-red-600 text-white rounded-md px-3 py-2 text-sm font-medium">
          Create Student
        </button>
      </div>

      <div className="itemContainer text-gray-900">
        {filteredData.length === 0 ? (
          <p>No results found.</p>
        ) : (
          <ul role="list" className="divide-y divide-gray-100">
            {currentPageData.map((person, index) => (
              <li key={index} className="flex justify-between gap-x-6 py-2 px-2">
                <div className="flex min-w-0 gap-x-2">
                  <img
                    className="h-10 w-10 flex-none rounded-full bg-gray-50"
                    src={person.imageUrl}
                    alt=""
                  />
                  <div className="min-w-0 flex-auto">
                    <p className="text-sm font-semibold leading-2 text-gray-900">
                      {person.name}
                    </p>
                    <p className="mt-1 truncate text-xs leading-1 text-gray-500">
                      {person.email}
                    </p>
                  </div>
                </div>
                <div className="hidden shrink-0 sm:flex sm:flex-col sm:items-end">
                  <p className="text-sm leading-2 text-gray-900">{person.role}</p>
                  <p className="mt-1 text-xs leading-1 text-gray-500">
                    Last seen{" "}
                    <time dateTime={person.lastSeenDateTime}>
                      {person.lastSeen}
                    </time>
                  </p>
                </div>
              </li>
            ))}
          </ul>
        )}

        <div className="flex items-center justify-between border-t border-black-700 bg-gray-300 px-4 py-3 sm:px1">
          <div className="hidden sm:flex sm:flex-1 sm:items-center sm:justify-between">
            <div>
              <p className="text-sm text-gray-700">
                Showing <span className="font-medium">{startIndex + 1}</span> to{" "}
                <span className="font-medium">
                  {Math.min(endIndex, filteredData.length)}
                </span>{" "}
                of <span className="font-medium">{filteredData.length}</span> results
              </p>
            </div>
            <div>
              <nav
                className="isolate inline-flex -space-x-px rounded-md shadow-sm"
                aria-label="Pagination"
              >
                <button
                  onClick={() => handlePageChange(currentPage - 1)}
                  disabled={!canGoPrevious}
                  className="relative inline-flex items-center rounded-l-md px-2 py-2 text-gray-400 ring-1 ring-inset ring-gray-400 hover:bg-red-400 focus:z-20 focus:outline-offset-0"
                >
                  <span className="sr-only">Previous</span>
                  {/* Previous Icon */}
                </button>
                {renderPagination()}
                <button
                  onClick={() => handlePageChange(currentPage + 1)}
                  disabled={!canGoNext}
                  className="relative inline-flex items-center rounded-r-md px-2 py-2 text-gray-400 ring-1 ring-inset ring-gray-400 hover:bg-red-400 focus:z-20 focus:outline-offset-0"
                >
                  <span className="sr-only">Next</span>
                  {/* Next Icon */}
                </button>
              </nav>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
};

export default Dashboard;

